package com.Ankita;

import java.util.Scanner;

public class Typeconversion {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        //Type Conversion
        float num = sc.nextFloat();
        System.out.println(num);

        int num1 = (int)(67.80);
        System.out.println(num1);
    }
}
